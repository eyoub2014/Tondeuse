package com.mowitnow;

/**
 * Enumération représentant les différentes commandes disponibles pour contrôler
 * une tondeuse.
 */

public enum Command {
	LEFT, // Commande pour tourner la tondeuse à gauche.
	RIGHT, // Commande pour tourner la tondeuse à droite.
	FORWARD // Commande pour faire avancer la tondeuse.
}